        bw_B2 = bw_B2 + (anm_bw_B2(i)*V(n+1,m+1) + bnm_bw_B2(i)*W(n+1,m+1))
            
        ch_A0 = ch_A0 + (anm_ch_A0(i)*V(n+1,m+1) + bnm_ch_A0(i)*W(n+1,m+1))
        ch_A1 = ch_A1 + (anm_ch_A1(i)*V(n+1,m+1) + bnm_ch_A1(i)*W(n+1,m+1))
        ch_B1 = ch_B1 + (anm_ch_B1(i)*V(n+1,m+1) + bnm_ch_B1(i)*W(n+1,m+1))
        ch_A2 = ch_A2 + (anm_ch_A2(i)*V(n+1,m+1) + bnm_ch_A2(i)*W(n+1,m+1))
        ch_B2 = ch_B2 + (anm_ch_B2(i)*V(n+1,m+1) + bnm_ch_B2(i)*W(n+1,m+1))
            
        cw_A0 = cw_A0 + (anm_cw_A0(i)*V(n+1,m+1) + bnm_cw_A0(i)*W(n+1,m+1))
        cw_A1 = cw_A1 + (anm_cw_A1(i)*V(n+1,m+1) + bnm_cw_A1(i)*W(n+1,m+1))
        cw_B1 = cw_B1 + (anm_cw_B1(i)*V(n+1,m+1) + bnm_cw_B1(i)*W(n+1,m+1))
        cw_A2 = cw_A2 + (anm_cw_A2(i)*V(n+1,m+1) + bnm_cw_A2(i)*W(n+1,m+1))
        cw_B2 = cw_B2 + (anm_cw_B2(i)*V(n+1,m+1) + bnm_cw_B2(i)*W(n+1,m+1))
            
    end do
end do
            
            
! determine the exact empirical values for the coefficients
bh = bh_A0 + bh_A1*cos(doy/365.25d0*2*pi) + bh_B1*sin(doy/365.25d0*2*pi) + &
                bh_A2*cos(doy/365.25d0*4*pi) + bh_B2*sin(doy/365.25d0*4*pi)
bw = bw_A0 + bw_A1*cos(doy/365.25d0*2*pi) + bw_B1*sin(doy/365.25d0*2*pi) + &
                bw_A2*cos(doy/365.25d0*4*pi) + bw_B2*sin(doy/365.25d0*4*pi)
ch = ch_A0 + ch_A1*cos(doy/365.25d0*2*pi) + ch_B1*sin(doy/365.25d0*2*pi) + &
                ch_A2*cos(doy/365.25d0*4*pi) + ch_B2*sin(doy/365.25d0*4*pi)
cw = cw_A0 + cw_A1*cos(doy/365.25d0*2*pi) + cw_B1*sin(doy/365.25d0*2*pi) + &
                cw_A2*cos(doy/365.25d0*4*pi) + cw_B2*sin(doy/365.25d0*4*pi)
    


! calculating the hydrostatic and wet mapping function
mfh = (1+(ah/(1+bh/(1+ch))))   /   (sin(el)+(ah/(sin(el)+bh/(sin(el)+ch))))
mfw = (1+(aw/(1+bw/(1+cw))))   /   (sin(el)+(aw/(sin(el)+bw/(sin(el)+cw))))


end subroutine