!        o zd: zenith distance (radians)
!
! OUTPUT:
!        o mfh: hydrostatic mapping factor
!        o mfw: wet mapping factor
!
!--------------------------------------------------------------------------
!
! translated to Fortran by Daniel Landskron (2018/01/31)
